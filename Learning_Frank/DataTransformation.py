# String zu Nummer
string = '123456'
print('String zu nummer', int(string), type(int(string)))
print('String to float:', float(string), type(float(string)))
print('String zu liste:', list(string), type(list(string)))
print('String zu Set:', set(string), type(set(string)))

number = 123456
print('Nummer zu String:', str(number), type(str(number)))